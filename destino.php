            <h2>Bizcocho de Limón y Merengue.</h2>
            <div class="recipe-image">
                <img src="img/MERENGUE.jpg" alt="Bizcocho de Limón y Merengue">
            </div>
            <p>¡Bienvenidos a mi blog de recetas! Hoy vamos a preparar un delicioso Bizcocho de Limón y Merengue. Esta receta es perfecta para cualquier ocasión y es muy fácil de hacer. ¡Vamos a ello!</p>
            
            <h3>Ingredientes para el bizcocho de limón</h3>
            <ul>
                <li>1 ½ tazas (190 g) de harina de trigo</li>
                <li>1 taza (200 g) de azúcar</li>
                <li>1 ½ cucharaditas de polvo de hornear</li>
                <li>½ cucharadita de sal</li>
                <li>½ taza (115 g) de mantequilla (a temperatura ambiente)</li>
                <li>1/3 taza (80 ml) de jugo de limón fresco</li>
                <li>2 huevos grandes</li>
                <li>1 cucharada de ralladura de limón</li>
                <li>1 cucharadita de extracto de vainilla</li>
                <li>½ taza (120 ml) de leche</li>
            </ul>

            <h3>Ingredientes para el relleno de limón</h3>
            <ul>
                <li>1 taza (200 g) de azúcar</li>
                <li>3 yemas de huevo</li>
                <li>1/3 taza (80 ml) de jugo de limón fresco</li>
                <li>2 cucharadas de ralladura de limón</li>
                <li>2 cucharadas de maicena (fécula de maíz)</li>
                <li>¼ taza (60 g) de mantequilla</li>
            </ul>
            
            <h3>Ingredientes para el merengue</h3>
            <ul>
                <li>4 claras de huevo</li>
                <li>1 taza (200 g) de azúcar</li>
                <li>¼ cucharadita de cremor tártaro (opcional, para estabilizar)</li>
                <li>1 cucharadita de extracto de vainilla</li>
            </ul>

            <h3>Instrucciones para preparar el bizcocho de limón</h3>
            <ol>
                <li>Precalienta tu horno a 180°C (350°F). Engrasa y enharina un molde para torta de 23 cm (9 pulgadas).</li>
                <li>En un tazón mediano, mezcla la harina, el polvo de hornear y la sal. Reserva.</li>
                <li>En un tazón grande, con una batidora eléctrica a velocidad media, bate la mantequilla hasta que esté cremosa. Agrega el azúcar y continúa batiendo hasta que la mezcla esté suave y ligera.</li>
                <li>Añade los huevos, uno a la vez, batiendo bien después de cada adición</li>
                <li> Agrega el jugo de limón, la ralladura de limón y el extracto de vainilla a la mezcla de mantequilla. Bate para combinar.                </li>
                <li>Alterna añadiendo los ingredientes secos y la leche a la mezcla de mantequilla, comenzando y terminando con los ingredientes secos. Mezcla hasta que esté bien combinado, pero no sobre mezcles.</li>
                <li>Vierte la masa en el molde preparado y alisa la superficie con una espátula. Hornea en el horno precalentado durante 25-30 minutos o hasta que un palillo insertado en el centro salga limpio.</li>
                <li>Deja enfriar el bizcocho en el molde durante 10 minutos, luego transfierelo a una rejilla para que se enfríe completamente.</li>    
            </ol> 

            <h3>Instrucciones para preparar el relleno de limón</h3>
            <ol>
        
                <li>En una cacerola mediana, mezcla el azúcar y la maicena.</li>
                <li>En un tazón separado, bate las yemas de huevo. Agrega el jugo de limón y la ralladura de limón. Vierte esta mezcla en la cacerola con el azúcar y la maicena.</li>
                <li> Cocina la mezcla a fuego medio, batiendo constantemente hasta que espese y comience a hervir. Una vez que haya hervido, cocina durante 1-2 minutos más.</li>
                <li> Retira del fuego y agrega la mantequilla, batiendo hasta que se derrita y se mezcle completamente.</li>
                <li>Deja enfriar el relleno a temperatura ambiente. Una vez enfriado, extiende el relleno sobre el bizcocho de limón.</li>
              
            </ol> 


            <h3>Instrucciones para preparar el merengue</h3>
            <ol>
        
                <li> En un tazón grande y limpio, bate las claras de huevo a velocidad media hasta que comiencen a espumar.</li>
                <li>Si usas, añade el cremor tártaro y continúa batiendo.</li>
                <li>Agrega el azúcar, una cucharada a la vez, mientras sigues batiendo a alta velocidad hasta que se formen picos firmes y el merengue esté brillante.</li>
                <li>  Añade el extracto de vainilla y bate brevemente para combinar.</li>
            </ol> 

            <h3>Instrucciones para montar la torta</h3>
            <ol>
        
                <li>Una vez que el bizcocho esté completamente enfriado, extiende el relleno de limón sobre el bizcocho.</li>
                <li> Extiende el merengue sobre el relleno de limón, asegurándote de cubrirlo completamente y creando picos decorativos si lo deseas.</li>
                <li>Coloca la torta bajo un grill (broiler) en el horno precalentado durante 1-2 minutos, o hasta que el merengue esté dorado. Vigila cuidadosamente para evitar que se queme.</li>
                <li>Deja enfriar la torta antes de servir. Esto permitirá que el merengue y el relleno se asienten adecuadamente.</li>
            </ol> 

            <h3>Mora Tips</h3>
            <ol>
        
                <li>Asegúrate de que el bizcocho esté completamente enfriado antes de agregar el relleno y el merengue para evitar que se derrame.</li>
                <li> Puedes usar una espátula para crear picos en el merengue antes de dorarlo, lo que le dará un aspecto más atractivo.</li>
            </ol> 



