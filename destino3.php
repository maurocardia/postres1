            <h3>Ingredientes para las fresas</h3>
            <ul>
                <li>fresas frescas </li>
                <li>2-3 cucharadas de azúcar (ajustar al gusto)</li>
            </ul>

            <h3>Ingredientes para la crema</h3>
            <ul>
                <li>1 taza (240 ml) de crema para batir</li>
                <li>2-3 cucharadas de azúcar glass (ajustar al gusto)</li>
                <li>1 cucharadita de extracto de vainilla (opcional)</li>
               
            </ul>
            
            

            <h3>Instrucciones para preparar las fresas</h3>
            <ol>
                <li>Lava las fresas bajo agua fría. Quita las hojas y corta las fresas en mitades o cuartos, dependiendo del tamaño y tu preferencia.</li>
                <li>Coloca las fresas en un tazón grande. Añade el azúcar y el jugo de limón (si lo estás usando). Mezcla suavemente para que las fresas se cubran uniformemente. Deja reposar durante al menos 15 minutos para que las fresas suelten su jugo y se mezclen los sabores.</li>
               átula. Hornea en el horno precalentado durante 25-30 minutos o hasta que un palillo insertado en el centro salga limpio.</li>
               

            <h3>Instrucciones para la crema</h3>
            <ol>
        
                <li>En un tazón grande y frío, vierte la crema para batir. Con una batidora eléctrica a velocidad media-alta, bate la crema hasta que comience a espesarse.

                </li>
                <li>Agrega el azúcar glass y el extracto de vainilla (si lo estás usando). Continúa batiendo hasta que la crema forme picos suaves. No batas demasiado, ya que podrías hacer mantequilla.</li>
                
            </ol> 


            <h3>Instrucciones para montar el postre</h3>
            <ol>
        
                <li> Coloca las fresas endulzadas en tazones o copas para postre.</li>
                <li>Sirve una generosa cantidad de crema batida sobre las fresas.</li>
                <li>Puedes decorar con hojas de menta fresca, un poco de ralladura de limón, o incluso un toque de chocolate rallado para un extra especial.</li>
                
            </ol> 

        
            <h3>Mora Tips</h3>
            <ol>
        
                <li>Si prefieres una crema más espesa, puedes añadir un poco de estabilizante para crema (como gelatina sin sabor) o batir un poco más.</li>
                <li> Puedes añadir otros frutos como arándanos o frambuesas a las fresas para variar el sabor.</li>
            </ol> 



